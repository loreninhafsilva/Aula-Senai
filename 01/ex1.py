def main():
    # Solicita ao usuário que insira um número inteiro
    numero = int(input("Digite um número inteiro: "))
    
    # Calcula o quadrado do número
    quadrado = numero ** 2
    
    # Apresenta o resultado
    print(f"O quadrado de {numero} é {quadrado}.")

if __name__ == "__main__":
    main()